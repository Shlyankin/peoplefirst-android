package rokolabs.com.peoplefirst.model;

public class CounsellingService {

    public int id;
    public String name;
    public String contact;

    public CounsellingService() {
    }

    public CounsellingService(int id, String name, String contact) {
        this.id = id;
        this.name = name;
        this.contact = contact;
    }
}
